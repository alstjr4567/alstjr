package basic;

public class NumberTest {

	public static void main(String[] args) {
		System.out.println(2 + 3); // 5
		System.out.println('2' + '3'); // 101
		System.out.println('2'-48 + '3'-48); // 문자 -> 숫자로 변환 아스키코드값 참고
		
		System.out.println("2" + "3"); // 23
		System.out.println(Integer.parseInt("2") + Integer.parseInt("3")); // 문자열 -> 숫자로 변환 위 문자처럼 하면(x)
		
	}
}
